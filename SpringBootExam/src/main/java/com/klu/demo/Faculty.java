package com.klu.demo;


public class Faculty {
	
	private int fid;
	private String fname;
	private String fgender;
	private String fdepartment;
	private String fdesgn;
	private int fsalary;
	public int getFid() {
		return fid;
	}
	public void setFid(int fid) {
		this.fid = fid;
	}
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public String getFgender() {
		return fgender;
	}
	public void setFgender(String fgender) {
		this.fgender = fgender;
	}
	public String getFdepartment() {
		return fdepartment;
	}
	public void setFdepartment(String fdepartment) {
		this.fdepartment = fdepartment;
	}
	public String getFdesgn() {
		return fdesgn;
	}
	public void setFdesgn(String fdesgn) {
		this.fdesgn = fdesgn;
	}
	public int getFsalary() {
		return fsalary;
	}
	public void setFsalary(int fsalary) {
		this.fsalary = fsalary;
	}
	
	
	
	

}
